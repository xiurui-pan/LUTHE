  function automatic logic [31:0] modSwitchToTorus32(
    input logic [31:0] mu,
    input logic [31:0] Msize
  );
    logic [63:0] interv;
    logic [63:0] phase64;
    
    // 与C++完全一致的算法：将mu从模Msize映射到Torus32
    interv = (64'h8000000000000000 / Msize) * 2;
    phase64 = mu * interv;
    return phase64[63:32];  // 返回高32位作为Torus32结果
  endfunction

